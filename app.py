def _dec(s: str) -> str:
    raw = "".join(s.split()).encode("ascii")
    return zlib.decompress(base64.b64decode(raw)).decode("utf-8")


def _load(fullname: str, src: str, package: str) -> None:
    mod = types.ModuleType(fullname)
    mod.__file__ = f"<{fullname}>"
    mod.__name__ = fullname
    mod.__package__ = package
    sys.modules[fullname] = mod
    exec(compile(src, fullname, "exec"), mod.__dict__)


