def _bytes_upload(f) -> tuple[bytes, str]:
    if f is None:
        return b"", ""
    f.seek(0)
    return f.read(), f.name


def _bytes_path(path_str: str) -> tuple[bytes, str, str]:
    """
    Lê bytes de um caminho local/rede (Windows).
    Retorna (bytes, nome, erro).
    """
    p = (path_str or "").strip().strip('"').strip("'")
    if not p:
        return b"", "", ""
    try:
        fp = Path(p)
        if not fp.is_file():
            return b"", "", f"Arquivo não encontrado: {p}"
        data = fp.read_bytes()
        return data, fp.name, ""
    except Exception as exc:
        return b"", "", f"Falha ao ler o caminho informado: {exc}"


def _bytes_input(uploaded, path_str: str) -> tuple[bytes, str, str]:
    """
    Prioridade:
    1) caminho colado (se preenchido)
    2) upload (se enviado)
    """
    data_p, nome_p, err = _bytes_path(path_str)
    if err:
        return b"", "", err
    if data_p:
        return data_p, nome_p, ""
    data_u, nome_u = _bytes_upload(uploaded)
    return data_u, nome_u, ""


